#boolean 4th - 조건문 if else, elif, else

a = 3
b = 2
c = 5
d = c - a

if a > b:                       # a가 b보다 크면 Hello 0_0
    print("Hello 0_0") 
if a > c:                        # a가 c보다 크면 What?
    print("What?")
elif a == d:                     # a가 2와 같다면 bye~!
    print("Bye~!")     
else:                            # a가 b보다 작으면 Hi!
    print("Hi!")